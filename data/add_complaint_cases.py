def generate_unique_id(prefix, num):
    """生成唯一ID"""
    return f"{prefix}_{str(num).zfill(3)}"

def random_date():
    """生成随机日期"""
    months = ["2024-01", "2024-03", "2024-06", "2024-09", "2024-12", "2025-01", "2025-03", "2025-06", "2025-09"]
    return random.choice(months)

