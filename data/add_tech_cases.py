def main():
    with open('cases_tech.json', 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    existing_cases = data["案例列表"]
    current_count = len(existing_cases)
    target_count = 100
    need_to_add = target_count - current_count
    
